package com.example.pizza;

class AuthResponse {
    Integer id;
    Integer token;

    public AuthResponse(Integer id, Integer token) {
        this.id = id;
        this.token = token;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getToken() {
        return token;
    }

    public void setToken(Integer token) {
        this.token = token;
    }
}
